# -*- encoding:utf-8 -*-
# Draw a 5-pointed star
import turtle
import time

turtle.color("red")
turtle.goto(0,0)

turtle.forward(100)
turtle.right(144)
turtle.forward(100)
turtle.right(144)
turtle.forward(100)
turtle.right(144)
turtle.forward(100)
turtle.right(144)
turtle.forward(100)
turtle.goto(144,0)

turtle.write("Welcome to Python")
time.sleep(3)
